CREATE OR REPLACE TRIGGER sa."TRIG_BYOP_OFFERS_OBJID"
BEFORE INSERT OR UPDATE on sa.TABLE_X_BYOP_OFFERS FOR EACH ROW
DECLARE

BEGIN


IF INSERTING
THEN

	IF :new.OBJID IS NULL
	THEN
	     SELECT sa.SEQU_BYOP_OFFERS.NEXTVAL
	       INTO :new.objid
	       FROM dual;


	END IF;

		:new.CREATED_ON := SYSDATE;

ELSIF UPDATING
THEN

	:new.MODIFIED_ON	:= SYSDATE;

END IF;

END;
/